function extractPublicInjectionTokens(sourceText) {
  const sourceFile = ts.createSourceFile(
    interfacePath,
    sourceText,
    ts.ScriptTarget.Latest,
    true,
    ts.ScriptKind.TS,
  );

  const tokens = [];

  function visit(node) {
    if (ts.isVariableStatement(node)) {
      for (const declaration of node.declarationList.declarations) {
        if (
          declaration.name.getText(sourceFile) === "PublicInjectionTokens" &&
          declaration.type &&
          ts.isTypeLiteralNode(declaration.type)
        ) {
          for (const member of declaration.type.members) {
            if (ts.isPropertySignature(member) && member.type) {
              tokens.push({
                property: member.name.getText(sourceFile),
                value: member.type.getText(sourceFile).replaceAll('"', ""),
              });
            }
          }
        }
      }
    }

    ts.forEachChild(node, visit);
  }

  visit(sourceFile);
  return tokens;
}

function toFunctionName(property) {
  const name = property.replace(/^\$/, "");
  return name
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/[^A-Za-z0-9_]/g, "_")
    .toLowerCase();
}

function render(tokens) {
  const lines = [
    "//// Generated by integrations/gleam/tool/generate_injection_tokens.mjs.",
    "//// Do not edit by hand.",
    "////",
    "//// Source: @types/interface.d.ts PublicInjectionTokens.",
    "",
    "import angular_ts/namespace",
    "import angular_ts/token.{type Token}",
    "import angular_ts/unsafe as js",
    "import angular_ts/wasm",
    "",
  ];

  for (const { property, value } of tokens) {
    if (property.endsWith("Provider")) {
      continue;
    }

    const fnName = toFunctionName(property);
    const typeName = typeMap.get(property) ?? "js.JsValue";
    const signature = `pub fn ${fnName}() -> Token(${typeName}) {`;
    if (signature.length > 88) {
      lines.push(`pub fn ${fnName}() -> Token(`);
      lines.push(`  ${typeName},`);
      lines.push(") {");
    } else {
      lines.push(signature);
    }
    lines.push(`  token.new("${value}")`);
    lines.push("}");
    lines.push("");
  }

  if (lines.at(-1) === "") lines.pop();

  return `${lines.join("\n")}\n`;
}

const output = render(
  extractPublicInjectionTokens(fs.readFileSync(interfacePath, "utf8")),
);
const check = process.argv.includes("--check");

if (check) {
  const current = fs.existsSync(outputPath)
    ? fs.readFileSync(outputPath, "utf8")
    : "";

  if (current !== output) {
    console.error(
      "Gleam injection tokens are stale. Run `make -C integrations/gleam generate-injection-tokens`.",
    );
    process.exit(1);
  }
} else {
  fs.writeFileSync(outputPath, output);
}
