package angularwasm

import (
	"os"
	"strings"
	"testing"
)

func TestBasicAppSourceHasNoManualScopeSetOrSync(t *testing.T) {
	source := readTestFile(t, "examples/basic_app/main.go")
	disallowed := []string{
		".scope.Set(",
		".scope.Sync(",
		".scope.Watch(",
		"app.scope.Set(",
		"app.scope.Sync(",
		"app.scope.Watch(",
		"update.Decode(",
		"app.sync()",
		`js.Global().Set("__ng_controller_GoTodoController"`,
	}

	for _, fragment := range disallowed {
		if strings.Contains(source, fragment) {
			t.Fatalf("handwritten todo app source contains manual scope bridge fragment %q", fragment)
		}
	}
}

func TestBasicAppSyncIsGenerated(t *testing.T) {
	source := readTestFile(t, "examples/basic_app/scope_sync_gen.go")

	required := []string{
		"Code generated by go generate; DO NOT EDIT.",
		"func registerController(app *TodoApp)",
		`js.Global().Set("__ng_controller_GoTodoController", api)`,
		"func (app *TodoApp) sync()",
		"angularwasm.SyncScope(",
		`angularwasm.ValueAt("items", app.items)`,
		`angularwasm.ValueAt("remainingCount", app.remainingCount())`,
		`angularwasm.ValueAt("newTodo", app.newTodo)`,
		`angularwasm.ValueAt("titleSeen", app.titleSeen)`,
		"func (app *TodoApp) bindScope(scopeName string)",
		"angularwasm.WatchValue[string](",
		"app.onNewTodoChanged",
		"app.sync()",
		"func (app *TodoApp) unbindScope()",
	}
	for _, fragment := range required {
		if !strings.Contains(source, fragment) {
			t.Fatalf("generated sync source missing %q", fragment)
		}
	}
}

func readTestFile(t *testing.T, path string) string {
	t.Helper()

	source, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("failed to read %s: %v", path, err)
	}

	return string(source)
}
